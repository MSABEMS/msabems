function changeImage_power() {
    var image = document.getElementById('myImage');
    if (image.src.match("images/remodeair/switchon")) {
        image.src = "images/remodeair/switchoff.png";
    } else {
        image.src = "images/remodeair/switchon.png";
    }
}


